# assigning valid options to a list

valid_options = ['r', 'p', 's']

# keeping counters below

games_counter = 0
player_score = 0
computer_score = 0
draw_score = 0
best_streak = 0

while True:     # Game logic in while loop
    player_choice = input(Fore.LIGHTWHITE_EX + 'Press "Enter" to play or "q" to quit: ').lower()
    if player_choice == 'q':
        print('Thanks for playing!')
        break
    elif player_choice == '':
        player_move = input('Choose [r]ock, [p]aper or [s]cissors: ').lower()
        if player_move in valid_options:
            computer_rng = random.randrange(len(valid_options))
            computer_move = valid_options[computer_rng]
            computer_print = ''
            player_print = ''
            if computer_move == 'r':  # converting computer move to string
                computer_print = rock
            elif computer_move == 'p':
                computer_print = paper
            elif computer_move == 's':
                computer_print = scissors
            if player_move == 'r':  # converting player move to string
                player_print = rock
            elif player_move == 'p':
                player_print = paper
            elif player_move == 's':
                player_print = scissors
            print('Rock...')
            time.sleep(0.3)
            print('Paper...')
            time.sleep(0.3)
            print('Scissors...')
            time.sleep(0.3)
            print('GO!')
            time.sleep(0.3)
            print(f'Your choice is:\n{player_print}')
            print(f'The computer has chosen:\n{computer_print}')
            if player_move == computer_move:  # game result logic for all possible outcomes
                print(Fore.LIGHTYELLOW_EX + 'Draw!')
                draw_score += 1
                best_streak = 0
            elif (player_move == 's' and computer_move == 'p') or \
                    (player_move == 'r' and computer_move == 's') or \
                    (player_move == 'p' and computer_move == 'r'):
                print(Fore.LIGHTGREEN_EX + 'You win!')
                player_score += 1
                best_streak += 1
            else:
                print(Fore.LIGHTRED_EX + 'You lose!')
                computer_score += 1
                best_streak = 0
            games_counter += 1
            print(Fore.CYAN + f'Total games: {games_counter}')
            print(Fore.CYAN + f'Score: Player: {player_score} : {computer_score} Computer')
            print(f'Draws: {draw_score}')
            print(f'Best streak: {best_streak}')
        else:
            print('Invalid input. Try again.')
            continue
    else:
        print(Fore.RED + 'Invalid input. Try again')
        continue
